def build_sample(text: str, label: str) -> dict:
    """단일 JSONL 샘플 생성"""
    return {
        "messages": [
            {
                "role": "user",
                "content": f"{SYSTEM_PROMPT}{text}",
            },
            {
                "role": "assistant",
                "content": label,
            },
        ]
    }


def generate_dataset(n: int) -> list[dict]:
    """
    n개의 샘플 생성 (각 클래스 균등 분배).
    n이 3의 배수가 아닌 경우 각 클래스 per_class개씩 할당 후
    나머지를 순환 추가.
    """
    labels = list(TEMPLATES.keys())  # positive, negative, neutral
    per_class = n // len(labels)
    remainder = n % len(labels)

    samples: list[dict] = []

    for i, label in enumerate(labels):
        pool = TEMPLATES[label].copy()
        count = per_class + (1 if i < remainder else 0)

        # pool보다 count가 크면 반복 샘플링
        chosen_texts: list[str] = []
        while len(chosen_texts) < count:
            shuffled = pool.copy()
            random.shuffle(shuffled)
            chosen_texts.extend(shuffled)
        chosen_texts = chosen_texts[:count]

        for text in chosen_texts:
            samples.append(build_sample(text, label))

    random.shuffle(samples)
    return samples


def write_jsonl(path: Path, records: list[dict]) -> None:
    with path.open("w", encoding="utf-8") as f:
        for record in records:
            f.write(json.dumps(record, ensure_ascii=False) + "\n")


def main() -> None:
    train_samples = generate_dataset(500)
    val_samples = generate_dataset(100)
    test_samples = generate_dataset(100)

    train_path = OUTPUT_DIR / "train.jsonl"
    val_path = OUTPUT_DIR / "val.jsonl"
    test_path = OUTPUT_DIR / "test.jsonl"

    write_jsonl(train_path, train_samples)
    write_jsonl(val_path, val_samples)
    write_jsonl(test_path, test_samples)

    # 검증
    for split, path, expected in [
        ("train", train_path, 500),
        ("val", val_path, 100),
        ("test", test_path, 100),
    ]:
        with path.open(encoding="utf-8") as f:
            lines = f.readlines()
        label_counts: dict[str, int] = {}
        for line in lines:
            obj = json.loads(line)
            label = obj["messages"][1]["content"]
            label_counts[label] = label_counts.get(label, 0) + 1
        print(
            f"{split:5s}: {len(lines):4d}줄 (expected {expected}) | "
            f"positive={label_counts.get('positive', 0):3d}, "
            f"negative={label_counts.get('negative', 0):3d}, "
            f"neutral={label_counts.get('neutral', 0):3d}"
        )
        assert len(lines) == expected, f"{split} 줄 수 불일치: {len(lines)} != {expected}"

    print("\n모든 파일이 정상적으로 생성되었습니다.")
    print(f"출력 경로: {OUTPUT_DIR.resolve()}")


if __name__ == "__main__":
    main()
